/*### Понимание задачи

**Условие задачи:**
Вы управляете парком развлечений и хотите определить **минимальный процент** посетителей, которые купили **все** доступные продукты питания. Каждый продавец сообщает процент посетителей, купивших его продукт.

**Примеры:**
1. **Один продукт (Popcorn, 76%)**:
   - Минимум: 76% (так как только один продукт)
2. **Два продукта (Hot Dogs 50%, Cotton Candy 50%)**:
   - Максимум: 50% (если все, кто купил хот-доги, также купили сладкую вату)
   - **Минимум: 0%** (возможно, что никто не купил оба продукта)
3. **Четыре продукта (Donuts 65%, Pretzel 80%, Pizza 80%, Ice Cream 90%)**:
   - Минимум: 15% (это процент посетителей, купивших все четыре продукта)

### Ключевая идея
Минимальный процент посетителей, купивших **все** продукты, вычисляется по формуле:
\[
\text{Minimum} = \left( \sum \text{проценты} \right) - (N - 1) \times 100\%
\]
где \( N \) — количество продуктов. Если результат отрицательный, минимум равен 0%.

**Формула работает так:**
- Суммируем все проценты покупок.
- Вычитаем \( (N-1) \times 100\% \) (так как каждый дополнительный продукт "перекрывает" 100% аудитории).
- Если результат меньше 0, значит, возможен случай, когда никто не купил все продукты (минимум = 0%).*/

//### Решение на JavaScript
//```javascript
function minPercentageBought(percentages) {
    const sum = percentages.reduce((total, p) => total + p, 0);
    const min = sum - (percentages.length - 1) * 100;
    return Math.max(min, 0); // Не может быть меньше 0%
}


/* ### Пошаговый разбор примера:
**Входные данные:** `[65, 80, 80, 90]` (Donuts 65%, Pretzel 80%, Pizza 80%, Ice Cream 90%)  
1. Сумма процентов: \( 65 + 80 + 80 + 90 = 315 \).  
2. Вычитаем \( (4 - 1) \times 100 = 300 \): \( 315 - 300 = 15 \).  
3. Результат: **15%** (это и есть минимальный процент посетителей, купивших все четыре продукта).  

### Проверка крайних случаев:
1. **Один продукт:**  
   - `[76]` → \( 76 - (1-1)*100 = 76\% \) (верно).  
2. **Два продукта по 50%:**  
   - `[50, 50]` → \( 50 + 50 - (2-1)*100 = 0\% \) (верно).  
3. **Все проценты > 100% (невозможно, но формула сработает):**  
   - `[110, 120]` → \( 230 - 100 = 130\% \) → но так как проценты не могут превышать 100, это некорректные входные данные.  

### Итог:
Функция `minPercentageBought` корректно вычисляет минимальный процент посетителей, купивших все продукты, используя принцип включений-исключений.*/

//**Пример вызова:**
//javascript
console.log(minPercentageBought([65, 80, 80, 90])); // 15
console.log(minPercentageBought([50, 50]));        // 0
console.log(minPercentageBought([76]));            // 76
